const express = require("express")
const app = express()
app.use(express.json())

let note = []

app.post("/notes",(req,res)=>{
    console.log(req.body)
    note.push(req.body)
    res.send("Data added")
})
app.get("/notes",(req,res)=>{
    res.send(note)
})
app.listen(3000,()=>{
    console.log("Server is running on port 3000")
})